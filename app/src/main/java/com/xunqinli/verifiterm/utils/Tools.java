    public static long CRC32Software(byte[] pData, int Length) {
        long nReg;//CRC寄存器
        long nTemp = 0;
        int i, n;
        nReg = 0xFFFFFFFF;//
        for (n = 0; n < Length; n++) {
            nReg ^= (long) pData[n];
            for (i = 0; i < 4; i++) {
                nTemp = Crc32Table[(byte) ((nReg >> 24) & 0xff)]; //取一个字节，查表
                nReg <<= 8; //丢掉计算过的头一个BYTE
                nReg ^= nTemp; //与前一个BYTE的计算结果异或
            }
        }
        return nReg;
    }

